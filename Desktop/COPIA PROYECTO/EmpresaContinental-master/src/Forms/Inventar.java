    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        int filaselec = jTable1.getSelectedRow();
        if (filaselec>=0) {
            JOptionPane.showMessageDialog(null, "esta seguro de eliminar a este proveedor.");
            modelo.removeRow(filaselec);
            Inventarios delete = new Inventarios();
            try {
                delete.eliminar(filaselec);
                JOptionPane.showMessageDialog(null, "Proveedor eliminado de forma permanente.");
            } catch (SQLException ex) {
                Logger.getLogger(Inventar.class.getName()).log(Level.SEVERE, null, ex);
            }

        }else{
            JOptionPane.showMessageDialog(null, "Por favor, seleccione una casilla.");
            jPanel2.setVisible(false);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        Inventarios enviar = new Inventarios();
        if (modifier==0 && filaSeleccionada ==0) {
            String Dato [] = new String[5];
            Dato[0] = String.valueOf(cont+1);
            Dato[1] = txtNombre.getText();
            Dato[2] = txtPrecio.getText();
            Dato[3] = txtExistencia.getText();
            Dato[4] = String.valueOf(Double.parseDouble(txtPrecio.getText())*Integer.parseInt(txtExistencia.getText()));

            JOptionPane.showMessageDialog(null, "Proveedor ingresado exitosamente!");
            modelo.addRow(Dato);
            jPanel2.setVisible(false);
           
            enviar.insertar(txtNombre.getText(), Double.parseDouble(txtPrecio.getText()), Integer.parseInt(txtExistencia.getText()));
            txtNombre.setText("");
            txtPrecio.setText("");
            txtExistencia.setText("");
            

            cont++;
        }
        else
        {
            int filaselect = jTable1.getSelectedRow();
            try {
                enviar.update(txtNombre.getText(), Double.parseDouble(txtPrecio.getText()), Integer.parseInt(txtExistencia.getText()),Integer.parseInt(jTable1.getValueAt(filaselect, 0).toString()));
            }   catch (SQLException ex) {
                Logger.getLogger(Inventar.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Proveedor modificado exitosamente!");
            jTable1.setValueAt(txtNombre.getText(),filaselect ,1);
            jTable1.setValueAt(txtPrecio.getText(),filaselect ,2);
            jTable1.setValueAt(txtExistencia.getText(),filaselect ,3);
            jPanel2.setVisible(false);
            txtNombre.setText("");
            txtPrecio.setText("");
            txtExistencia.setText("");
        
            filaSeleccionada=0;
            modifier=0;
        }
        //  System.out.println("SE HA ENVIADO A LA DATA BASE");

    }//GEN-LAST:event_btnAceptarActionPerformed

