 public void mostrar_Proveedores(){
        Conexion con = new Conexion();
        Connection cn = con.ConectarBaseDatos();
        String sql = "SELECT id_Inventario,Nombre,Precio, Existencia, total FROM inventario ";
        
        modelo.addColumn("No. Registro");
        modelo.addColumn("Nombre");
        modelo.addColumn("Precio");
        modelo.addColumn("Existencia");
        modelo.addColumn("Total");
        
        jTable1.setModel(modelo);
        
        String[] datos = new String[5];
        Statement st;
        
        try {
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            while (rs.next()) {                
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                modelo.addRow(datos);
            }
        } catch (SQLException ex) {
            Logger.getLogger(Inventar.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

